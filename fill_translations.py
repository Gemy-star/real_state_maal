def process_po_file(filepath):
    """Read a .po file, fill in empty msgstr entries, and write back."""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    import re

    def replace_empty_msgstr(match):
        full_match = match.group(0)
        msgid_raw = match.group(1)

        # Unescape the msgid to look it up in our dictionary
        # Handle multi-line msgids (joined with space in po format)
        # The msgid might span multiple lines with continuation strings
        msgid_lines = re.findall(r'"((?:[^"\\]|\\.)*)"', msgid_raw)
        msgid = ''.join(
            line.replace('\\n', '\n').replace('\\"', '"').replace('\\\\', '\\')
            for line in msgid_lines
        )

        if msgid in translations:
            translation = translations[msgid]
            # Escape the translation for po format
            escaped = translation.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n"\n"')
            return full_match.rstrip() + f'"{escaped}"'

        return full_match  # leave unchanged if no translation found

    # Match msgid blocks followed by empty msgstr ""
    # Pattern: msgid "..." (possibly multiline) followed by msgstr ""
    pattern = re.compile(
        r'(msgid\s+"(?:[^"\\]|\\.)*"(?:\s*"(?:[^"\\]|\\.)*")*)\s*\nmsgstr ""\s*\n',
        re.MULTILINE
    )

    def replacer(match):
        msgid_block = match.group(1)
        # Extract all quoted strings from msgid block
        msgid_lines = re.findall(r'"((?:[^"\\]|\\.)*)"', msgid_block)
        msgid = ''.join(
            line.replace('\\n', '\n').replace('\\"', '"').replace('\\\\', '\\')
            for line in msgid_lines
        )

        if msgid and msgid in translations:
            translation = translations[msgid]
            escaped = translation.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n"\n"')
            return f'{msgid_block}\nmsgstr "{escaped}"\n'

        return match.group(0)

    new_content = pattern.sub(replacer, content)

    filled = sum(1 for _ in pattern.finditer(content))
    matched = len(re.findall(r'msgstr "(?!").+"', new_content)) - len(re.findall(r'msgstr "(?!").+"', content))

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)

    return filled, matched


